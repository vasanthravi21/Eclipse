package UnaryOperator;

public class Unaryoperator {
    public static void main(String args[]){
        /*Unary operator
            1. preincrement
            2.postinceremnt
         */
        int a=55;
        //a++ postincerment   ==> a = a+1;
        //++a preincerment

        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
    }
}
