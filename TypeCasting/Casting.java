package TypeCasting;

public class Casting {
    public static void main(String args[]){
        //TypeCasting.Casting
        /* TypeCasting.Casting is of two types
                1. Widening TypeCasting.Casting
                2. Narrowing TypeCasting.Casting
         */
        int a=10;
        double b=a,d=22.263;
        int c=(int)d;
        System.out.println("Int : "+a);
        System.out.println("Double : "+b);
        System.out.println("Double : "+d);
        System.out.println("Int : "+c);
    }

}
